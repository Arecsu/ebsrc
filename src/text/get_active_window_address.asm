
GET_ACTIVE_WINDOW_ADDRESS:
	BEGIN_C_FUNCTION
	LDA WINDOW_HEAD
	CMP #$FFFF
	BNE @UNKNOWN0
	LDA #.LOWORD(DEFAULT_WINDOW)
	BRA @UNKNOWN1
@UNKNOWN0:
	LDA CURRENT_FOCUS_WINDOW
	ASL
	TAX
	LDA WINDOW_EXISTENCE_TABLE,X
	LDY #.SIZEOF(window_stats)
	JSL MULT168
	CLC
	ADC #.LOWORD(WINDOW_STATS_TABLE)
@UNKNOWN1:
	END_C_FUNCTION
